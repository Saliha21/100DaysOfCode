// else if statement.

var degree=30;
if(degree>=90){
  document.write("Your result:"+" A ");
}
else if(degree>=80){
  document.write("Your result:"+ " B ");
}
else if(degree>=70){
  document.write("Your result:"+ " C ");
}
else if(degree>=60){
  document.write("Your result:"+ " D ");
}
else{
  document.write("Your result:"+ " F ");
}



