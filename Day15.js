/* The syntax 
if (condition) {
block of code to be executed if the condition is true
} */

// if statement.
var degree=50;
if(degree>=50){
  document.write("good");
}
// else statement.
var degree=40;
if(degree>=50){
  document.write("good");
}
else{
  document.write("<br>"+ "bad");
}


